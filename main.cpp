#include <iostream>

using namespace std;

int main()
{
	int iNum1 = 20;
	int iNum2 = 10;

	float fNum1 = 20.1f;
	float fNum2 = 10.0f;

	cout << iNum1 + iNum2 << endl;
	cout << iNum1 - iNum2 << endl;
	cout << iNum1 * iNum2 << endl;
	cout << iNum1 / iNum2 << endl;
	cout << iNum1 % iNum2 << endl << endl;

	cout << fNum1 + fNum2 << endl;
	cout << fNum1 - fNum2 << endl;
	cout << fNum1 * fNum2 << endl;
	cout << fNum1 / fNum2 << endl;

	return 0;
}