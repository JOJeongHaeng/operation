#include <iostream>

using namespace std;

int main()
{
	int IntNum1 = 20;
	int IntNum2 = 10;

	float FloatNum1 = 20.1f;
	float FloatNum2 = 10.0f;

	cout << IntNum1 + IntNum2 << endl;
	cout << IntNum1 - IntNum2 << endl;
	cout << IntNum1 * IntNum2 << endl;
	cout << IntNum1 / IntNum2 << endl;
	cout << IntNum1 % IntNum2 << endl << endl;

	cout << FloatNum1 + FloatNum2 << endl;
	cout << FloatNum1 - FloatNum2 << endl;
	cout << FloatNum1 * FloatNum2 << endl;
	cout << FloatNum1 / FloatNum2 << endl;

	return 0;
}